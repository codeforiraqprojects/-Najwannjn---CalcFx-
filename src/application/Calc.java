package application;

public class Calc {
	
	public static Integer sum(Integer x,Integer y){
    return x+y;
}
	
	public static Integer sub(Integer x,Integer y){
	    return x-y;
	}
	
	public static Integer multiply(Integer x,Integer y){
	    return x*y;
	}
	
	public static Integer division(Integer x,Integer y){
	    return x/y;
	}
}
